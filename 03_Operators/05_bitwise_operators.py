# Bitwise operators perform operations on binary representations of integers.

# Operator	Description	Example
# &	Bitwise AND	a & b
# `	`	Bitwise OR	`a	b`
# ^	Bitwise XOR	a ^ b
# ~	Bitwise NOT	~a
# <<	Left shift	a << 2
# >>	Right shift	a >> 2

# Bitwise Operators
a = 10  # Binary: 1010
b = 4   # Binary: 0100

# && &

print("a & b:", a & b)
print("a | b:", a | b) # 1110